# ─── Config ────────────────────────────────────────────────────────────
OUTPUT_DIR = "/home/ubuntu/github-proxy/uploads/slides"
PUBLIC_BASE = "https://mgc-pass-proxy.duckdns.org/img"
MODEL = "nano-banana-pro-preview"
API_BASE = "https://generativelanguage.googleapis.com/v1beta"
REQUEST_TIMEOUT_SEC = 180


def _get_gemini_key() -> str:
    """Read GEMINI_API_KEY from Doppler. Raises RuntimeError if missing."""
    try:
        result = subprocess.run(
            ["doppler", "secrets", "get", "GEMINI_API_KEY",
             "--project", "nippo-syncro-kun", "--config", "dev", "--plain"],
            capture_output=True, text=True, check=True,
        )
    except subprocess.CalledProcessError as e:
        raise RuntimeError(
            f"Could not read GEMINI_API_KEY from Doppler: {e.stderr.strip()}"
        ) from e
    key = result.stdout.strip()
    if not key:
        raise RuntimeError("GEMINI_API_KEY is empty in Doppler nippo-syncro-kun/dev")
    return key


# ─── Prompt templates ──────────────────────────────────────────────────
# These are general templates. The ImagePipeline class fills in the
# client-specific details (company_concepts, openings, etc.) when building
# the final prompts so each client gets something unique that matches
# their actual business.
#
# Style directives are shared across all prompts:
#   - Japanese setting (assumed location_hint default = "Kyoto, Japan")
#   - Modern minimalist aesthetic
#   - Natural photography style (NOT illustration, NOT 3D render)
#   - Subtle theme_accent color wash in shadows
#   - Explicit AI-failure exclusions: no text overlays, no readable logos,
#     no watermarks, realistic hands with correct fingers, not "AI-rendered"
#     look

_SHARED_STYLE_BLOCK = (
    "Editorial magazine photography style, shallow depth of field, soft "
    "natural lighting, photojournalism quality. NO text overlays, NO "
    "visible logos or brand names, NO watermarks, realistic skin texture, "
    "realistic human hands with correct finger counts, NOT an AI-rendered "
    "look."
)

