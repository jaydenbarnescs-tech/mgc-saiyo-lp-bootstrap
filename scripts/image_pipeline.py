# ─── Data classes ──────────────────────────────────────────────────────
@dataclass
class ImageSpec:
    """One image to generate, with its target path in lp_content JSON."""
    key: str           # stable identifier for logging + filename (e.g., "hero", "pos0")
    jsonb_path: str    # dotted path in lp_content (e.g., "hero.bg_image")
    prompt: str        # the full Gemini prompt, pre-rendered


@dataclass
class ImagePipelineResult:
    """Per-image result with URL, size, timing."""
    key: str
    ok: bool
    jsonb_path: str = ""
    url: str = ""
    filename: str = ""
    local_path: str = ""
    size_bytes: int = 0
    elapsed_sec: float = 0.0
    error: str = ""


# ─── Gender alternation helper ─────────────────────────────────────────
# For LPs with 2+ positions, alternate the gender of the position imagery
# so we don't have 3 photos of men or 3 of women in a row.
_GENDER_ROTATION = ["woman", "man", "woman", "man", "woman", "man"]


class ImagePipeline:
    """
    Orchestrates concept-aware LP image generation for a single client.

    Usage:
        pipe = ImagePipeline(slug="cloq", client_name="株式会社CLOQ", ...)
        results = pipe.generate_all()  # blocks ~22s for typical LP
        # results is a dict: { jsonb_path: ImagePipelineResult }
    """

    def __init__(
        self,
        slug: str,
        client_name: str,
        industry: str,
        theme_accent: str,
        company_concepts: dict,
        openings: list[dict],
        *,
        generate_employee_headshots: bool = True,
        output_dir: str = OUTPUT_DIR,
        public_base: str = PUBLIC_BASE,
    ):
        if not slug or not slug.replace("-", "").isalnum():
            raise ValueError(f"invalid slug: {slug!r}")
        if not openings:
            raise ValueError("at least one opening is required")
        self.slug = slug
        self.client_name = client_name
        self.industry = industry
        self.theme_accent = theme_accent or "#2673b8"
        self.concepts = company_concepts
        self.openings = openings
        self.gen_employees = generate_employee_headshots
        self.output_dir = output_dir
        self.public_base = public_base
        self._gemini_key: Optional[str] = None

        # Derived defaults from company_concepts
        self.location_hint = company_concepts.get("location_hint", "Kyoto, Japan")
        self.business_type = company_concepts.get(
            "business_type", "professional services"
        )
        # Translate business type to a scene-appropriate English phrase for
        # the whiteboard diagram in the hero shot
        self.business_workflow = self._infer_workflow(industry)
        self.values_en = self._format_values()
        self.conversation_topic = company_concepts.get(
            "conversation_topic", "honest work"
        )

        os.makedirs(self.output_dir, exist_ok=True)

    # ─── Internal helpers ──────────────────────────────────────────────

    def _get_key(self) -> str:
        if self._gemini_key is None:
            self._gemini_key = _get_gemini_key()
        return self._gemini_key

    def _infer_workflow(self, industry: str) -> str:
        """Map industry to a scene-appropriate workflow topic for the hero."""
        table = {
            "採用コンサルティング": "recruitment workflow",
            "人材紹介": "candidate matching",
            "人材紹介事業": "candidate matching",
            "recruitment consulting": "recruitment workflow",
            "製造業": "production process",
            "manufacturing": "production process",
            "IT": "product roadmap",
            "ソフトウェア": "product roadmap",
            "software": "product roadmap",
            "建設業": "project schedule",
            "construction": "project schedule",
            "広告": "campaign timeline",
            "advertising": "campaign timeline",
            "マーケティング": "campaign timeline",
            "marketing": "campaign timeline",
        }
        for k, v in table.items():
            if k in industry:
                return v
        return "business workflow"

    def _format_values(self) -> str:
        """Render company_concepts.values[] as a natural English phrase."""
        values = self.concepts.get("values", [])
        if not values:
            return "honest communication and flat hierarchy"
        # Japanese values get a parenthetical English gloss
        value_glosses = {
            "本音": "honest communication (本音)",
            "フラット": "flat hierarchy",
            "フラットチーム": "flat team culture",
            "在宅可": "flexible remote work",
            "服装自由": "casual dress code",
            "実務": "hands-on work",
            "現場": "on-the-ground execution",
            "素直": "genuine openness",
        }
        glossed = [value_glosses.get(v, v) for v in values[:3]]
        return " and ".join(glossed)

    # ─── Prompt builders ───────────────────────────────────────────────

    def _build_hero_prompt(self) -> str:
        return HERO_PROMPT_TEMPLATE.format(
            location_hint=self.location_hint,
            business_type_en=self.business_type,
            business_workflow=self.business_workflow,
            theme_accent=self.theme_accent,
            style=_SHARED_STYLE_BLOCK,
        )

    def _build_about_prompt(self) -> str:
        return ABOUT_PROMPT_TEMPLATE.format(
            location_hint=self.location_hint,
            conversation_topic=self.conversation_topic,
            values_en=self.values_en,
            theme_accent=self.theme_accent,
            style=_SHARED_STYLE_BLOCK,
        )

    def _build_position_prompt(self, idx: int, opening: dict) -> str:
        """
        Build a prompt for a position image. Tries to match the scene to the
        role semantically (customer-facing vs writer vs engineer etc.) based
        on the role title keywords.
        """
        gender = _GENDER_ROTATION[idx % len(_GENDER_ROTATION)]
        title = opening.get("title", "")
        role_en = opening.get("en", "team member")

        # Role-aware environment + action
        environment_detail = (
            "Desk has an open notebook with handwritten notes, a white "
            "ceramic coffee cup, and a small potted plant."
        )
        light_side = "left"
        outfit = (
            "a simple cream-colored sweater over a collared shirt"
            if gender == "woman"
            else "a charcoal merino v-neck sweater over a simple white t-shirt"
        )

        # Keyword-based role_action heuristic — picks a scene that fits
        # what the employee would actually be doing day-to-day
        title_lower = title.lower() + " " + role_en.lower()
        if any(k in title for k in ["支援", "サポート", "採用"]) or "support" in title_lower or "recruit" in title_lower:
            role_action = (
                "engaged in a video call with a job candidate (visible as a "
                "blurred silhouette on the laptop screen, no identifying "
                "features), gesturing warmly while explaining something, "
                "genuine professional smile"
            )
        elif any(k in title for k in ["ライター", "クリエイター", "コピー"]) or "writ" in title_lower or "copy" in title_lower or "content" in title_lower:
            role_action = (
                "deep in creative focus at a standing desk writing ideas in "
                "a paper notebook with a fountain pen while a laptop beside "
                "them shows a text editor with blurred copy-in-progress. "
                "Post-it notes with scribbled word ideas (text blurred) on "
                "a cork board behind them"
            )
            environment_detail = (
                "Warm morning light streams from a large window overlooking "
                "a traditional " + self.location_hint + " street."
            )
        elif any(k in title_lower for k in ["engineer", "developer", "エンジニア", "開発"]):
            role_action = (
                "focused on dual monitors showing code (text blurred, not "
                "readable), one hand on a mechanical keyboard, expression "
                "of quiet concentration"
            )
        elif any(k in title_lower for k in ["design", "デザイン"]):
            role_action = (
                "sketching a concept on a drawing tablet with a stylus, "
                "design software open on a monitor beside them (UI blurred)"
            )
        elif any(k in title_lower for k in ["sales", "営業", "セールス"]):
            role_action = (
                "on a phone call while looking at a laptop screen, taking "
                "notes on a paper notepad, warm confident expression"
            )
        else:
            # Generic fallback: meeting / collaboration scene
            role_action = (
                "in the middle of focused work at a modern office desk, "
                "engaged with a laptop and notebook, calm professional "
                "expression"
            )

        return POSITION_PROMPT_TEMPLATE.format(
            gender=gender,
            location_hint=self.location_hint,
            role_name=title,
            role_en=role_en,
            role_action=role_action,
            environment_detail=environment_detail,
            light_side=light_side,
            outfit=outfit,
            theme_accent=self.theme_accent,
            style=_SHARED_STYLE_BLOCK,
        )

    def _build_employee_prompt(self, idx: int, opening: dict) -> str:
        """
        Build a headshot prompt for a placeholder employee. Alternates gender
        with the position images (if position 0 shows a woman, employee 0's
        headshot is also a woman for consistency).
        """
        gender = _GENDER_ROTATION[idx % len(_GENDER_ROTATION)]
        title = opening.get("title", "").lower()

        # Role-matched appearance
        if "クリエイター" in title or "writ" in title or "copy" in title:
            # Creative writer vibe
            if gender == "woman":
                hair_desc = "Shoulder-length black hair in a natural wavy style"
                appearance = "minimal makeup, warm skin tone"
                outfit = "a simple charcoal turtleneck with minimal jewelry"
                expression = "thoughtful but warm expression"
                mood = "creative, focused, genuine"
                personality_hint = "takes craft seriously"
                bg_context = "cafe or co-working space"
            else:
                hair_desc = "Short neatly-cut black hair, clean-shaven, wearing glasses with thin modern frames"
                appearance = "warm skin tone, relaxed posture"
                outfit = "a charcoal merino v-neck sweater over a white t-shirt"
                expression = "thoughtful but warm expression"
                mood = "creative, focused, genuine"
                personality_hint = "thinks deeply about their writing"
                bg_context = "cafe or co-working space"
            gaze = "looking directly at the camera with a subtle smile"
        else:
            # Customer-facing / support / general professional vibe
            if gender == "woman":
                hair_desc = "Shoulder-length black hair in a natural style"
                appearance = "minimal makeup, warm skin tone"
                outfit = "a soft cream-colored turtleneck sweater with a simple silver necklace"
                expression = "natural warm smile"
                mood = "genuine, approachable, and confident"
                personality_hint = "genuinely cares about helping people"
                bg_context = "office interior"
            else:
                hair_desc = "Short neatly-cut black hair, clean-shaven"
                appearance = "warm skin tone, open posture"
                outfit = "a navy cardigan over a simple white dress shirt"
                expression = "natural warm smile"
                mood = "genuine, approachable, and confident"
                personality_hint = "genuinely cares about helping people"
                bg_context = "office interior"
            gaze = "looking slightly off-camera in a candid moment"

        return EMPLOYEE_HEADSHOT_TEMPLATE.format(
            gender=gender,
            location_hint=self.location_hint,
            expression=expression,
            gaze=gaze,
            hair_desc=hair_desc,
            appearance_detail=appearance,
            outfit=outfit,
            bg_context=bg_context,
            theme_accent=self.theme_accent,
            mood=mood,
            personality_hint=personality_hint,
            style=_SHARED_STYLE_BLOCK,
        )

    # ─── Spec assembly ─────────────────────────────────────────────────

    def _assemble_specs(self) -> list[ImageSpec]:
        """Return the full list of ImageSpecs to generate for this LP."""
        specs = [
            ImageSpec(
                key="hero",
                jsonb_path="hero.bg_image",
                prompt=self._build_hero_prompt(),
            ),
            ImageSpec(
                key="about",
                jsonb_path="about.photo",
                prompt=self._build_about_prompt(),
            ),
        ]
        for i, opening in enumerate(self.openings):
            specs.append(
                ImageSpec(
                    key=f"pos{i}",
                    jsonb_path=f"openings.items.{i}.image",
                    prompt=self._build_position_prompt(i, opening),
                )
            )
            if self.gen_employees:
                specs.append(
                    ImageSpec(
                        key=f"emp{i}",
                        jsonb_path=f"openings.items.{i}.detail.employee.photo",
                        prompt=self._build_employee_prompt(i, opening),
                    )
                )
        return specs

    # ─── Generation ────────────────────────────────────────────────────

    def _generate_one(self, spec: ImageSpec) -> ImagePipelineResult:
        """Generate a single image via Gemini REST API."""
        t0 = time.time()
        url = f"{API_BASE}/models/{MODEL}:generateContent?key={self._get_key()}"
        body = {"contents": [{"parts": [{"text": spec.prompt}]}]}
        req = urllib.request.Request(
            url,
            data=json.dumps(body).encode("utf-8"),
            headers={"Content-Type": "application/json"},
            method="POST",
        )
        try:
            with urllib.request.urlopen(req, timeout=REQUEST_TIMEOUT_SEC) as resp:
                data = json.loads(resp.read().decode("utf-8"))
        except urllib.error.HTTPError as e:
            err = e.read().decode("utf-8", errors="replace")[:500]
            return ImagePipelineResult(
                key=spec.key, ok=False, jsonb_path=spec.jsonb_path,
                error=f"HTTP {e.code}: {err}",
                elapsed_sec=round(time.time() - t0, 1),
            )
        except Exception as e:
            return ImagePipelineResult(
                key=spec.key, ok=False, jsonb_path=spec.jsonb_path,
                error=f"{type(e).__name__}: {e}",
                elapsed_sec=round(time.time() - t0, 1),
            )

        candidates = data.get("candidates", [])
        if not candidates:
            return ImagePipelineResult(
                key=spec.key, ok=False, jsonb_path=spec.jsonb_path,
                error=f"no candidates (prompt may have been blocked): {json.dumps(data)[:200]}",
                elapsed_sec=round(time.time() - t0, 1),
            )

        parts = candidates[0].get("content", {}).get("parts", [])
        image_part = next((p for p in parts if "inlineData" in p), None)
        if not image_part:
            text_parts = [p.get("text", "")[:100] for p in parts if "text" in p]
            return ImagePipelineResult(
                key=spec.key, ok=False, jsonb_path=spec.jsonb_path,
                error=f"no image in response, only text: {text_parts}",
                elapsed_sec=round(time.time() - t0, 1),
            )

        mime = image_part["inlineData"].get("mimeType", "image/jpeg")
        raw = base64.b64decode(image_part["inlineData"]["data"])
        ext = "jpg" if "jpeg" in mime else ("png" if "png" in mime else "bin")
        h = hashlib.sha1(f"{self.slug}-{spec.key}-{time.time()}".encode()).hexdigest()[:12]
        filename = f"{self.slug}_{spec.key}_{h}.{ext}"
        path = os.path.join(self.output_dir, filename)
        with open(path, "wb") as f:
            f.write(raw)

        return ImagePipelineResult(
            key=spec.key,
            ok=True,
            jsonb_path=spec.jsonb_path,
            url=f"{self.public_base}/{filename}",
            filename=filename,
            local_path=path,
            size_bytes=len(raw),
            elapsed_sec=round(time.time() - t0, 1),
        )

    def generate_all(self, max_parallel: int = 6) -> dict[str, ImagePipelineResult]:
        """
        Generate every image for this LP in parallel and return a dict
        keyed by jsonb_path (so the caller can directly feed it into a
        jsonb_set UPDATE statement).

        Also duplicates the `openings.items[N].image` result into
        `openings.items[N].detail.hero_bg` since those are two separate
        fields in the lp_content schema that should render the same photo.
        """
        specs = self._assemble_specs()
        results: dict[str, ImagePipelineResult] = {}

        print(
            f"[image_pipeline] {self.slug}: generating {len(specs)} images "
            f"via {MODEL}, max_parallel={max_parallel}",
            flush=True,
        )
        t0 = time.time()
        with ThreadPoolExecutor(max_workers=max_parallel) as pool:
            futures = {pool.submit(self._generate_one, s): s for s in specs}
            for future in as_completed(futures):
                spec = futures[future]
                try:
                    result = future.result(timeout=REQUEST_TIMEOUT_SEC + 20)
                except Exception as e:
                    result = ImagePipelineResult(
                        key=spec.key, ok=False,
                        jsonb_path=spec.jsonb_path,
                        error=f"future error: {e}",
                    )
                results[result.jsonb_path] = result
                status = "✓" if result.ok else "✗"
                print(
                    f"  {status} {spec.key:6s} [{result.elapsed_sec:5.1f}s] "
                    f"{result.jsonb_path:48s} "
                    f"{'→ ' + result.url if result.ok else '— ' + result.error[:80]}",
                    flush=True,
                )

        # Duplicate opening.image URLs into opening.detail.hero_bg paths
        # since those are two separate schema fields that should show the
        # same image on the main LP and the job detail page respectively
        for i in range(len(self.openings)):
            src = f"openings.items.{i}.image"
            dst = f"openings.items.{i}.detail.hero_bg"
            if src in results and results[src].ok:
                r = results[src]
                results[dst] = ImagePipelineResult(
                    key=f"{r.key}_detail_hero",
                    ok=True,
                    jsonb_path=dst,
                    url=r.url,
                    filename=r.filename,
                    local_path=r.local_path,
                    size_bytes=r.size_bytes,
                    elapsed_sec=0.0,  # duplicate, no generation cost
                )

        total = time.time() - t0
        ok_count = sum(1 for r in results.values() if r.ok)
        print(
            f"[image_pipeline] {self.slug}: {ok_count}/{len(results)} "
            f"paths populated in {total:.1f}s wall clock",
            flush=True,
        )
        return results

    def build_update_sql(self, results: dict[str, ImagePipelineResult]) -> str:
        """
        Given a results dict, build a single SQL UPDATE statement that
        jsonb_sets every successful image URL into the cloq lp_content row.

        Chains jsonb_set calls from innermost to outermost. Skips failed
        generations (jsonb path is left unchanged).
        """
        successful = [(path, r) for path, r in results.items() if r.ok]
        if not successful:
            raise RuntimeError("no successful image generations — nothing to update")

        # Build the chained jsonb_set expression
        expr = "content"
        for jsonb_path, r in successful:
            # Convert dotted path like "openings.items.0.image" to
            # postgres jsonb path array like '{openings,items,0,image}'
            parts = jsonb_path.split(".")
            pg_path = "{" + ",".join(parts) + "}"
            # The new value is the URL as a jsonb string literal
            # Dollar-quote the URL to avoid escaping issues
            expr = (
                f"jsonb_set(\n    {expr},\n    '{pg_path}',\n    "
                f"$$\"{r.url}\"$$::jsonb\n  )"
            )

        return f"""-- Auto-generated by image_pipeline.py for slug={self.slug!r}
-- Replaces {len(successful)} image paths in lp_content.
update public.lp_content
set content = {expr},
    updated_at = now()
where lp_slug = '{self.slug}';"""


# ─── CLI entry point ───────────────────────────────────────────────────
def _cli():
    """
    Command-line entry point. Reads JSON config from stdin and runs the
    pipeline. Used by bootstrap.py via subprocess, so bootstrap.py can
    stay in its own process and call image_pipeline.py as a clean boundary.

    Input JSON (on stdin):
        {
            "slug": "cloq",
            "client_name": "株式会社CLOQ",
            "industry": "採用コンサルティング",
            "theme_accent": "#2673b8",
            "company_concepts": {
                "business_type": "人材紹介事業",
                "values": ["本音", "フラットチーム", "実務"],
                "location_hint": "Kyoto, Japan",
                "conversation_topic": "recruitment strategy"
            },
            "openings": [
                {"title": "採用支援スタッフ", "en": "Recruitment Support"},
                {"title": "採用クリエイター", "en": "Recruitment Writer"}
            ],
            "generate_employee_headshots": true
        }

    Output JSON (on stdout):
        {
            "ok": true,
            "slug": "cloq",
            "urls": {
                "hero.bg_image": "https://...",
                "about.photo": "https://...",
                ...
            },
            "update_sql": "update public.lp_content set ..."
        }
    """
    cfg = json.load(sys.stdin)
    pipe = ImagePipeline(
        slug=cfg["slug"],
        client_name=cfg["client_name"],
        industry=cfg.get("industry", ""),
        theme_accent=cfg.get("theme_accent", "#2673b8"),
        company_concepts=cfg.get("company_concepts", {}),
        openings=cfg.get("openings", []),
        generate_employee_headshots=cfg.get("generate_employee_headshots", True),
    )
    results = pipe.generate_all()
    urls = {path: r.url for path, r in results.items() if r.ok}
    errors = {path: r.error for path, r in results.items() if not r.ok}
    output = {
        "ok": all(r.ok for r in results.values()),
        "slug": pipe.slug,
        "urls": urls,
        "errors": errors,
        "update_sql": pipe.build_update_sql(results) if urls else "",
    }
    print(json.dumps(output, ensure_ascii=False, indent=2))
    sys.exit(0 if output["ok"] else 1)


if __name__ == "__main__":
    _cli()
