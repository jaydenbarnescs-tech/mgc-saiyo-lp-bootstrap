#!/usr/bin/env python3
"""
image_pipeline.py — concept-aware LP image generation via Gemini nano-banana-pro-preview.

Part of mgc-saiyo-lp-bootstrap. Replaces the v0 approach (raw scrape URLs
from the client's site reused verbatim) with concept-aware image generation
that KEEPS the style from the reference site (colors via theme tokens) but
GENERATES new imagery based on what the company actually does and values.

This enforces RULE 14 from SKILL.md: "Images should be 融合 of original
style + generated content, NOT reused homepage photos."

═══ How it's used from bootstrap.py ═══

    from image_pipeline import ImagePipeline, ImageSpec

    pipe = ImagePipeline(
        slug='cloq',
        client_name='株式会社CLOQ',
        industry='採用コンサルティング',
        theme_accent='#2673b8',
        company_concepts={
            'business_type': '人材紹介事業 / 採用コンサルティング',
            'values': ['本音', 'フラットチーム', '在宅可・服装自由'],
            'location_hint': 'Kyoto',
            'culture_tone': 'young, honest, hands-on',
        },
        openings=[
            {'title': '採用支援スタッフ', 'en': 'Recruitment Support', 'employee_name': '田中 美咲'},
            {'title': '採用クリエイター', 'en': 'Recruitment Writer', 'employee_name': '佐藤 健太'},
        ],
    )
    urls = pipe.generate_all()  # blocks for ~20-25s total (parallel)
    # urls is a dict keyed by slot path:
    # { 'hero.bg_image': 'https://mgc-pass-proxy.duckdns.org/img/cloq_hero_*.jpg',
    #   'about.photo': '...',
    #   'openings.items.0.image': '...',
    #   'openings.items.0.detail.hero_bg': '...',  # same URL as .image
    #   'openings.items.0.detail.employee.photo': '...',
    #   ...
    # }

The caller (bootstrap.py) then builds a single UPDATE statement that
jsonb_sets each path into lp_content. No Supabase Storage calls — images
are written directly to /home/ubuntu/github-proxy/uploads/slides/ and
served via nginx at mgc-pass-proxy.duckdns.org/img/.

═══ Parallelism ═══

All generations run in parallel via ThreadPoolExecutor(max_workers=6).
Empirical timings on Gemini nano-banana-pro-preview: ~20-22s per image,
parallel wall time matches the slowest single call. For a 2-opening LP
with 2 placeholder employees, total generation time is ~22s wall clock
vs ~130s sequential.

═══ Infrastructure dependencies ═══

- GEMINI_API_KEY must be set in Doppler nippo-syncro-kun/dev
- /home/ubuntu/github-proxy/uploads/slides/ must be writable as the
  executing user (Jayden's ubuntu account works)
- nginx must serve /img/ from that path (see /etc/nginx/sites-enabled/
  mgc-proxy line 81 — alias directive)
- Python 3.8+ (uses concurrent.futures, no external deps beyond stdlib)

═══ Why not Supabase Storage? ═══

See SKILL.md categories 10-12 and the hand-off notes in the cloq build
retrospective. Short version: the lp-images bucket exists in
pglaffdnhixmabcjdxbi but (a) the service_role key in Vercel env is
broken/stale, (b) the anon key in Doppler is for a different project
(isp-approach-bot), and (c) upload/route.ts ALREADY routes through
mgc-pass-proxy for the admin editor's uploads. Keeping image generation
on the same path is architecturally consistent — all LP images live at
mgc-pass-proxy.duckdns.org/img/ regardless of whether they were
AI-generated by this skill or uploaded manually by a client via the
admin editor. One storage path, one set of public URLs, one backup target.
"""
